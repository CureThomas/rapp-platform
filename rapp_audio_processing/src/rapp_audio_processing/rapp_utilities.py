import sys
import time
import os

class Utilities:

  # Service callback for detecting silence
  def cleanup(self, files): 
    for f in files:
      if os.path.isfile(f) == False:
        return "Error: " + f + " is not a file"
      command = "rm " + f
      com_res = os.system(command)
      if com_res != 0:
        return "Error: Server rm malfunctioned"
    return True

